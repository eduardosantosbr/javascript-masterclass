/**
 * There are only six falsey values in JavaScript:
 * undefined, null, NaN, 0, "", and false of course.
 */

console.log(`undefined: ${!!undefined}`);
console.log(`null: ${!!null}`);
console.log(`NaN: ${!!NaN}`);
console.log(`0: ${!!0}`);
console.log(`""(empty string): ${!!""}`);
console.log(`false: ${!!false}`);